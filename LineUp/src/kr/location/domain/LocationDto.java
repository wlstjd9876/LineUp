package kr.location.domain;

public class LocationDto {
	String loc_name;
	String loc_address;
	String loc_phone;
	String loc_photo1;
	String loc_photo2;
	String loc_content;
	int loc_num;
	String loc_id;
	public String getLoc_name() {
		return loc_name;
	}
	public void setLoc_name(String loc_name) {
		this.loc_name = loc_name;
	}
	public String getLoc_address() {
		return loc_address;
	}
	public void setLoc_address(String loc_address) {
		this.loc_address = loc_address;
	}
	public String getLoc_phone() {
		return loc_phone;
	}
	public void setLoc_phone(String loc_phone) {
		this.loc_phone = loc_phone;
	}
	public String getLoc_photo1() {
		return loc_photo1;
	}
	public void setLoc_photo1(String loc_photo1) {
		this.loc_photo1 = loc_photo1;
	}
	public String getLoc_photo2() {
		return loc_photo2;
	}
	public void setLoc_photo2(String loc_photo2) {
		this.loc_photo2 = loc_photo2;
	}
	public String getLoc_content() {
		return loc_content;
	}
	public void setLoc_content(String loc_content) {
		this.loc_content = loc_content;
	}
	public int getLoc_num() {
		return loc_num;
	}
	public void setLoc_num(int loc_num) {
		this.loc_num = loc_num;
	}
	public String getLoc_id() {
		return loc_id;
	}
	public void setLoc_id(String loc_id) {
		this.loc_id = loc_id;
	}
}